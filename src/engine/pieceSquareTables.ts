// Mirror table vertically for Black (flip ranks)
export function mirror(table: number[]): number[] {
  const out = new Array(64);
  for (let r = 0; r < 8; r++) {
    for (let f = 0; f < 8; f++) {
      out[r * 8 + f] = table[(7 - r) * 8 + f];
    }
  }
  return out;
}

